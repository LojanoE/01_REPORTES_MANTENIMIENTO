-- 3. Funcion para actualizar stock automaticamente
CREATE OR REPLACE FUNCTION update_inventory_stock()
RETURNS TRIGGER AS $$
BEGIN
    IF (TG_OP = 'INSERT') THEN
        IF (NEW.type = 'IN') THEN
            UPDATE bodegas_inventory SET current_stock = current_stock + NEW.quantity, updated_at = NOW() WHERE id = NEW.item_id;
        ELSIF (NEW.type = 'OUT') THEN
            UPDATE bodegas_inventory SET current_stock = current_stock - NEW.quantity, updated_at = NOW() WHERE id = NEW.item_id;
        END IF;
    ELSIF (TG_OP = 'UPDATE') THEN
        IF (OLD.type = 'IN') THEN
            UPDATE bodegas_inventory SET current_stock = current_stock - OLD.quantity, updated_at = NOW() WHERE id = OLD.item_id;
        ELSIF (OLD.type = 'OUT') THEN
            UPDATE bodegas_inventory SET current_stock = current_stock + OLD.quantity, updated_at = NOW() WHERE id = OLD.item_id;
        END IF;
        IF (NEW.type = 'IN') THEN
            UPDATE bodegas_inventory SET current_stock = current_stock + NEW.quantity, updated_at = NOW() WHERE id = NEW.item_id;
        ELSIF (NEW.type = 'OUT') THEN
            UPDATE bodegas_inventory SET current_stock = current_stock - NEW.quantity, updated_at = NOW() WHERE id = NEW.item_id;
        END IF;
    ELSIF (TG_OP = 'DELETE') THEN
        IF (OLD.type = 'IN') THEN
            UPDATE bodegas_inventory SET current_stock = current_stock - OLD.quantity, updated_at = NOW() WHERE id = OLD.item_id;
        ELSIF (OLD.type = 'OUT') THEN
            UPDATE bodegas_inventory SET current_stock = current_stock + OLD.quantity, updated_at = NOW() WHERE id = OLD.item_id;
        END IF;
        RETURN OLD;
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- 4. Trigger
DROP TRIGGER IF EXISTS tr_update_stock ON bodegas_transactions;
CREATE TRIGGER tr_update_stock
AFTER INSERT OR UPDATE OR DELETE ON bodegas_transactions
FOR EACH ROW
EXECUTE FUNCTION update_inventory_stock();

