-- 7. FUNCION AUXILIAR: Recalcular stock desde transacciones
-- =============================================================================

CREATE OR REPLACE FUNCTION recalculate_inventory_stock()
RETURNS void AS $$
DECLARE
    inv RECORD;
    calc_stock NUMERIC;
BEGIN
    FOR inv IN SELECT id, initial_stock FROM bodegas_inventory LOOP
        SELECT COALESCE(
            SUM(CASE WHEN type = 'IN' THEN quantity ELSE -quantity END),
            0
        ) INTO calc_stock
        FROM bodegas_transactions
        WHERE item_id = inv.id;

        UPDATE bodegas_inventory
        SET current_stock = inv.initial_stock + calc_stock,
            updated_at = NOW()
        WHERE id = inv.id;
    END LOOP;
END;
$$ LANGUAGE plpgsql;

