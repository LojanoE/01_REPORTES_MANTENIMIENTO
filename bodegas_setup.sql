-- 4. TRIGGER: Actualizacion automatica de stock
-- =============================================================================

CREATE OR REPLACE FUNCTION update_inventory_stock()
RETURNS TRIGGER AS $$
BEGIN
    IF TG_OP = 'INSERT' THEN
        UPDATE bodegas_inventory
        SET current_stock = current_stock +
                CASE WHEN NEW.type = 'IN' THEN NEW.quantity ELSE -NEW.quantity END,
            updated_at = NOW()
        WHERE id = NEW.item_id;
        RETURN NEW;

    ELSIF TG_OP = 'UPDATE' THEN
        -- Revertir efecto del valor anterior
        UPDATE bodegas_inventory
        SET current_stock = current_stock +
                CASE WHEN OLD.type = 'IN' THEN -OLD.quantity ELSE OLD.quantity END,
            updated_at = NOW()
        WHERE id = OLD.item_id;
        -- Aplicar efecto del nuevo valor
        UPDATE bodegas_inventory
        SET current_stock = current_stock +
                CASE WHEN NEW.type = 'IN' THEN NEW.quantity ELSE -NEW.quantity END,
            updated_at = NOW()
        WHERE id = NEW.item_id;
        RETURN NEW;

    ELSIF TG_OP = 'DELETE' THEN
        UPDATE bodegas_inventory
        SET current_stock = current_stock +
                CASE WHEN OLD.type = 'IN' THEN -OLD.quantity ELSE OLD.quantity END,
            updated_at = NOW()
        WHERE id = OLD.item_id;
        RETURN OLD;
    END IF;

    RETURN NULL;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS tr_update_stock ON bodegas_transactions;
CREATE TRIGGER tr_update_stock
    AFTER INSERT OR UPDATE OR DELETE ON bodegas_transactions
    FOR EACH ROW
    EXECUTE FUNCTION update_inventory_stock();

